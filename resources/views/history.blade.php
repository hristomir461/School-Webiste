                    <p class="mb-4" style="font-size: 13px;">Историята на Професионална гимназия по механотехника, електроника, телекомуникации и транспорт „Христо Ботев” започва през лятото на 1949 г. когато на основание на Постановление на Министерския съвет и Заповед на МНП се закрива Механотехническата гимназия в гр. Разград и същата се премества в гр. Шумен. На 5.10.1949 г. тържествено се открива новата учебна година в сградата на бившата Мъжка гимназия с 10 учители и 224 ученици , разпределени в 6 паралелки.
                        На 1.09.1953 г. гимназията се преименува в Техникум по механоелектротехника, а на 1.09.1954 г. – в Техникум по машиностроене и уредостроене.
                        Преломен момент в развитието на техникума е построяването на машиностроителен завод в Шумен (днес „Мадара” АД). За да отговори на необходимостта от специалисти, техникумът значително повишава първоначално определения си прием, включително и вечерна паралелка.</p>

